    private void btnAddUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddUpdateActionPerformed
        if(mapText != null) {
            mapText.colour = (NamedColour)cbColour.getSelectedItem();
            mapText.fontSize = -1;
            mapText.point[0] = (Integer)spnX1.getValue();
            mapText.point[1] = (Integer)spnY1.getValue();
            mapText.orientation[0] = cbHorizontal.getSelectedIndex();
            mapText.orientation[1] = cbVertical.getSelectedIndex();
            mapText.rotate = cbRotated.isSelected();
            mapText.text = txtText.getText();
            mapText.fontSize = (Integer)spnFontSize.getValue();
            mapText.layer = (Integer)spnLayer.getValue();
            fireMapChange(new MapChangeEvent(MapChangeEvent.UPDATE, mapText));
        } else {
            mapText = new MapText(txtText.getText(), (Integer)spnFontSize.getValue(), (Integer)spnX1.getValue(), (Integer)spnY1.getValue(), cbHorizontal.getSelectedIndex(), cbVertical.getSelectedIndex(), cbRotated.isSelected());
            mapText.colour = (NamedColour)cbColour.getSelectedItem();
            mapText.layer = (Integer)spnLayer.getValue();
            fireMapChange(new MapChangeEvent(MapChangeEvent.ADD, mapText));
        }
    }//GEN-LAST:event_btnAddUpdateActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if(mapText != null) {
            fireMapChange(new MapChangeEvent(MapChangeEvent.REMOVE, mapText));
        } else {
            JOptionPane.showMessageDialog(this, "Must select text", "Removing", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        fireMapChange(new MapChangeEvent(MapChangeEvent.CANCEL, mapText));
    }//GEN-LAST:event_btnCancelActionPerformed


