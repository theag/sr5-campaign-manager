    @Override
    public void setObject(MapText object) {
        mapText = object;
        btnAddUpdate.setText("Update");
        btnRemove.setVisible(true);
        txtText.setText(mapText.text);
        cbColour.setSelectedItem(mapText.colour);
        spnX1.setValue(mapText.point[0]);
        spnY1.setValue(mapText.point[1]);
        cbHorizontal.setSelectedIndex(mapText.orientation[0]);
        cbVertical.setSelectedIndex(mapText.orientation[1]);
        cbRotated.setSelected(mapText.rotate);
        spnFontSize.setValue(mapText.fontSize);
        spnLayer.setValue(mapText.layer);
        setBackground(highlight);
    }

    @Override
    public void clearData() {
        mapText = null;
        btnAddUpdate.setText("Add");
        btnRemove.setVisible(false);
        cbColour.setSelectedIndex(0);
        spnX1.setValue(0);
        spnY1.setValue(0);
        cbHorizontal.setSelectedIndex(0);
        cbVertical.setSelectedIndex(2);
        spnFontSize.setValue(12);
        cbRotated.setSelected(false);
        txtText.setText("");
        spnLayer.setValue(0);
        setBackground(plain);
    }

