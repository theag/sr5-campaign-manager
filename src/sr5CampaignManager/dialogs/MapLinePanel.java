    private void btnAddUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddUpdateActionPerformed
        if(mapLine != null) {
            mapLine.colour = (NamedColour)cbColour.getSelectedItem();
            mapLine.lineWidth = ((LineWidthComboBoxModel)cbLineWidth.getModel()).getValue();
            mapLine.points[0] = (Integer)spnX1.getValue();
            mapLine.points[1] = (Integer)spnY1.getValue();
            mapLine.points[2] = (Integer)spnX2.getValue();
            mapLine.points[3] = (Integer)spnY2.getValue();
            mapLine.layer = (Integer)spnLayer.getValue();
            fireMapChange(new MapChangeEvent(MapChangeEvent.UPDATE, mapLine));
        } else {
            mapLine = new MapLine((Integer)spnX1.getValue(), (Integer)spnY1.getValue(), (Integer)spnX2.getValue(), (Integer)spnY2.getValue());
            mapLine.colour = (NamedColour)cbColour.getSelectedItem();
            mapLine.lineWidth = ((LineWidthComboBoxModel)cbLineWidth.getModel()).getValue();
            mapLine.layer = (Integer)spnLayer.getValue();
            fireMapChange(new MapChangeEvent(MapChangeEvent.ADD, mapLine));
        }
    }//GEN-LAST:event_btnAddUpdateActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if(mapLine != null) {
            fireMapChange(new MapChangeEvent(MapChangeEvent.REMOVE, mapLine));
        } else {
            JOptionPane.showMessageDialog(this, "Must select line", "Removing", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        fireMapChange(new MapChangeEvent(MapChangeEvent.CANCEL, mapLine));
    }//GEN-LAST:event_btnCancelActionPerformed


