    private void cbLineWidthItemSelected() {
        LineWidthComboBoxModel model = (LineWidthComboBoxModel)cbLineWidth.getModel();
        if(model.isCustomSelected()) {
            NumberSelectorDialog nsd = new NumberSelectorDialog(null, "Custom Line Width..", model.getValue(), false);
            if(nsd.showDialog() == NumberSelectorDialog.OK) {
                model.setValue(nsd.getNumber());
            }
        }
    }
    
    @Override
    public void setObject(MapLine object) {
        mapLine = object;
        btnAddUpdate.setText("Update");
        btnRemove.setVisible(true);
        cbColour.setSelectedItem(mapLine.colour);
        boolean found = false;
        for(int i = 0; i < LineWidthComboBoxModel.values.length; i++) {
            if(mapLine.lineWidth == LineWidthComboBoxModel.values[i]) {
                cbLineWidth.setSelectedIndex(i);
                found = true;
                break;
            }
        }
        if(!found) {
            ((LineWidthComboBoxModel)cbLineWidth.getModel()).setValue(mapLine.lineWidth);
            cbLineWidth.setSelectedIndex(LineWidthComboBoxModel.values.length);
        }
        spnX1.setValue(mapLine.points[0]);
        spnY1.setValue(mapLine.points[1]);
        spnX2.setValue(mapLine.points[2]);
        spnY2.setValue(mapLine.points[3]);
        spnLayer.setValue(mapLine.layer);
        setBackground(highlight);
    }

    @Override
    public void clearData() {
        mapLine = null;
        btnAddUpdate.setText("Add");
        btnRemove.setVisible(false);
        cbColour.setSelectedIndex(0);
        spnX1.setValue(0);
        spnY1.setValue(0);
        spnX2.setValue(0);
        spnY2.setValue(0);
        cbLineWidth.setSelectedIndex(LineWidthComboBoxModel.defaultIndex);
        spnLayer.setValue(0);
        setBackground(plain);
    }

    @Override
    public boolean isOfType(String typeName) {
        return typeName.compareTo("MapLine") == 0;
    }
